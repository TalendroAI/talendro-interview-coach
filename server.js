app.post('/api/chat', async (req, res) => {
  try {
    const {
      message,
      sessionType,
      documents,
      customerEmail,
      conversationHistory
    } = req.body;

    const anthropicApiKey = process.env.ANTHROPIC_API_KEY;

    if (!anthropicApiKey) {
      return res.status(500).json({ error: 'Anthropic API key not configured' });
    }

    const systemPrompt =
      (typeof SYSTEM_PROMPTS !== 'undefined' && SYSTEM_PROMPTS[sessionType]) ||
      (typeof SYSTEM_PROMPTS !== 'undefined' && SYSTEM_PROMPTS.quick_prep) ||
      'You are Talendro Interview Coach. Be concise, practical, and specific.';

    // Build context from documents (only added once)
    let context = '';
    if (documents) {
      if (documents.resume) {
        context += `\n\nCANDIDATE RESUME:\n${documents.resume}`;
      }
      if (documents.jobDescription) {
        context += `\n\nJOB DESCRIPTION:\n${documents.jobDescription}`;
      }
      if (documents.companyUrl) {
        context += `\n\nCOMPANY WEBSITE URL: ${documents.companyUrl}`;
      }
    }

    let messages = [];

    if (conversationHistory && conversationHistory.length > 0) {
      messages = conversationHistory.map((msg, index) => {
        if (index === 0 && msg.role === 'user' && context) {
          return {
            role: msg.role,
            content: `${msg.content}\n\n---\nCONTEXT:${context}`
          };
        }
        return { role: msg.role, content: msg.content };
      });

      messages.push({ role: 'user', content: message });
    } else {
      messages = [
        {
          role: 'user',
          content: context ? `${message}\n\n---\nCONTEXT:${context}` : message
        }
      ];
    }

    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': anthropicApiKey,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-3-5-sonnet-latest',
        max_tokens: 4096,
        system: systemPrompt,
        messages
      })
    });

    if (!response.ok) {
      const error = await response.json().catch(() => ({}));
      console.error('Anthropic API error (chat):', error);
      throw new Error(
        `Anthropic API error: ${error.error?.message || response.statusText}`
      );
    }

    const data = await response.json();
    const assistantMessage =
      data.content && data.content[0] && data.content[0].text
        ? data.content[0].text
        : '';

    return res.json({ message: assistantMessage });
  } catch (error) {
    console.error('Chat error:', error);
    return res.status(500).json({ error: 'Failed to get AI response' });
  }
});

