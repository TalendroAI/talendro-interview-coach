/**
 * Create a Google Doc with the transcript and return the URL
 */
async function createTranscriptDoc(email, sessionType, transcript) {
  if (!docs || !drive) {
    console.warn('Google Docs not configured - skipping doc creation');
    return null;
  }
  
  try {
    // Format session type for display
    const sessionTypeLabels = {
      'quick_prep': 'Quick Prep',
      'full_mock': 'Full Mock Interview',
      'audio_mock': 'Audio Mock Interview'
    };
    const sessionLabel = sessionTypeLabels[sessionType] || sessionType;
    
    // Create timestamp
    const now = new Date();
    const dateStr = now.toLocaleDateString('en-US', { 
      year: 'numeric', 
      month: 'long', 
      day: 'numeric' 
    });
    const timeStr = now.toLocaleTimeString('en-US', { 
      hour: '2-digit', 
      minute: '2-digit' 
    });
    
    // Create the document title
    const docTitle = `Talendro Interview Coach - ${sessionLabel} - ${email} - ${dateStr}`;
    
    // Create a new Google Doc using Drive API (in the Shared Drive)
    const createResponse = await drive.files.create({
      requestBody: {
        name: docTitle,
        mimeType: 'application/vnd.google-apps.document',
        parents: [TRANSCRIPT_FOLDER_ID]
      },
      fields: 'id',
      supportsAllDrives: true
    });
    
    const documentId = createResponse.data.id;
    console.log(`Created Google Doc: ${documentId}`);
    
    // Build the document content
    const headerText = `TALENDRO™ INTERVIEW COACH\n${sessionLabel} Results\n\n`;
    const metaText = `Email: ${email}\nDate: ${dateStr} at ${timeStr}\nSession Type: ${sessionLabel}\n\n${'─'.repeat(50)}\n\n`;
    const footerText = `\n\n${'─'.repeat(50)}\n\nGenerated by Talendro™ Interview Coach\nwww.talendro.com\n`;
    
    const fullContent = headerText + metaText + transcript + footerText;
    
    // Insert content into the document
    await docs.documents.batchUpdate({
      documentId: documentId,
      requestBody: {
        requests: [
          {
            insertText: {
              location: { index: 1 },
              text: fullContent
            }
          },
          // Format the header (first line)
          {
            updateParagraphStyle: {
              range: { startIndex: 1, endIndex: headerText.indexOf('\n') + 1 },
              paragraphStyle: {
                namedStyleType: 'HEADING_1',
                alignment: 'CENTER'
              },
              fields: 'namedStyleType,alignment'
            }
          }
        ]
      }
    });
    
    // Make the document viewable by anyone with the link
    await drive.permissions.create({
      fileId: documentId,
      requestBody: {
        role: 'reader',
        type: 'anyone'
      },
      supportsAllDrives: true
    });
    
    // Get the document URL
    const docUrl = `https://docs.google.com/document/d/${documentId}/view`;
    console.log(`Google Doc created and shared: ${docUrl}`);
    
    return docUrl;
    
  } catch (error) {
    console.error('Error creating Google Doc:', error);
    // Don't throw - we don't want to fail the whole request if doc creation fails
    return null;
  }
}

